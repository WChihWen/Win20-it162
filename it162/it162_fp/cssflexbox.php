    <h3>What is CSS Flexbox?</h3>
    <p>
        Flexbox is a CSS 3 web layout model. It is in the W3C's Candidate recommendation stage. The flex layout allows responsive elements within a container to be automatically arranged depending upon screen size.
    </p>

